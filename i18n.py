# All available translations. Language codes match Bambuddy's own supported
# set (frontend/src/i18n/index.ts's SUPPORTED_LNGS), kept fully lowercase
# (e.g. "pt-br", not "pt-BR") for consistent case-insensitive matching
# against query params / cookies in resolve_locale below.
TRANSLATIONS = {
    "en": EN,
    "de": DE,
    "es": ES,
    "fr": FR,
    "ja": JA,
    "it": IT,
    "ko": KO,
    "pt-br": PT_BR,
    "zh-cn": ZH_CN,
    "zh-tw": ZH_TW,
    "tr": TR,
}

DEFAULT_LANG = "en"
SUPPORTED_LANGS = sorted(TRANSLATIONS)


def get_translation(lang: str, key: str, **kwargs: Any) -> str:
    """Get a translation string by key with optional interpolation.

    Args:
        lang: Language code (e.g., 'en', 'de').
        key: Dot-separated key path (e.g., 'web.config_heading').
        **kwargs: Values to interpolate into the string via str.format().

    Returns:
        Translated string, or the key itself if not found in either the
        requested language or the English fallback.
    """
    translations = TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANG])

    keys = key.split(".")
    value: Any = translations
    for k in keys:
        if isinstance(value, dict) and k in value:
            value = value[k]
        else:
            # Key not found in the requested language — fall back to English.
            value = TRANSLATIONS[DEFAULT_LANG]
            for k2 in keys:
                if isinstance(value, dict) and k2 in value:
                    value = value[k2]
                else:
                    return key  # Not found in the fallback either.
            break

    if isinstance(value, str):
        try:
            return value.format(**kwargs)
        except KeyError:
            return value

    return key


class Translator:
    """Helper class for translations bound to a specific language."""

    def __init__(self, lang: str = DEFAULT_LANG):
        self.lang = lang if lang in TRANSLATIONS else DEFAULT_LANG

    def t(self, key: str, **kwargs: Any) -> str:
        """Translate a key."""
        return get_translation(self.lang, key, **kwargs)


def resolve_locale(
    accept_language_header: str | None,
    query_lang: str | None = None,
    cookie_lang: str | None = None,
) -> str:
    """Resolve the active language for a request.

    Priority: an explicit ?lang= query override, then a previously-set
    language cookie, then the browser's Accept-Language header, then the
    default. Query/cookie values are matched case-insensitively against the
    supported set; Accept-Language is parsed permissively (region subtags
    like "en-US" fall back to their base "en").

    Only used by the Flask web app — the Tkinter desktop GUI has no HTTP
    request to read a header/cookie from, so it resolves its language from
    the OS locale instead (see gui_locale() below).
    """
    for candidate in (query_lang, cookie_lang):
        if candidate and candidate.strip().lower() in TRANSLATIONS:
            return candidate.strip().lower()

    if accept_language_header:
        for part in accept_language_header.split(","):
            code = part.split(";")[0].strip().lower()
            if not code:
                continue
            if code in TRANSLATIONS:
                return code
            base = code.split("-")[0]
            if base in TRANSLATIONS:
                return base

    return DEFAULT_LANG


def gui_locale(env_lang: str | None = None) -> str:
    """Resolve the Tkinter desktop GUI's language from the OS locale.

    There's no browser/header to read, so this uses Python's own locale
    detection (LANG/LC_ALL on Linux/macOS, the user's regional settings on
    Windows via locale.getlocale()) and falls back to English. `env_lang`
    lets callers/tests inject an override without touching real env vars or
    the OS locale database.
    """
    import locale

    candidate = env_lang
    if not candidate:
        try:
            candidate = locale.getlocale()[0]
        except Exception:
            candidate = None

    if not candidate:
        return DEFAULT_LANG

    normalized = candidate.strip().lower().replace("_", "-")
    if normalized in TRANSLATIONS:
        return normalized
    base = normalized.split("-")[0]
    return base if base in TRANSLATIONS else DEFAULT_LANG
